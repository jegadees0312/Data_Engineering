# Comparison Operators
x,y = 1,2

is_greater = x>y
is_equal = x==y

print(is_greater)
print(is_equal)

# Logical Operators
a,b = True,False

# logical AND
print(a and b)

# Logical or
print(a or b)

# Logical NOT
print(not b)

# Boolean
#Bool of integer
print("bool for 1: ",bool(1))
print("bool for 0: ",bool(0))

# Bool for String
print("Bool for string: ",bool('hello'))
print("Bool for Empty string: ",bool(''))

